package controlStatement;

public class IfUse {

	public static void main(String[] args) {
		char gender='D';
		
		if(gender=='M')
			
		{
			System.out.println("Gender is Male");
		}
		else if(gender=='F'){
			System.out.println("Gender is Female");
		}
		else
		{
			System.out.println("Invalid Choice");
		}

	}

}
